    global.getCardLang = function () {
        try {
            return window.localStorage.getItem('siteLanguage') || 'ru';
        } catch (e) {
            return 'ru';
        }
    };

    global.getCardTypeLabel = function (typeKey, lang) {
        if (!typeKey) return '';
        if (!lang || lang === 'ru') {
            return typeof CARD_TYPES !== 'undefined' && CARD_TYPES[typeKey] ? CARD_TYPES[typeKey] : typeKey;
        }
        const map = CARD_TYPES_I18N[lang];
        return (map && map[typeKey]) || (typeof CARD_TYPES !== 'undefined' ? CARD_TYPES[typeKey] : typeKey);
    };

    global.getLocalizedCardFields = function (card, lang) {
        if (!card || !lang || lang === 'ru') {
            return { name: card.name, ability: card.ability };
        }
        const t = CARD_TEXT[lang] && CARD_TEXT[lang][card.id];
        if (!t) return { name: card.name, ability: card.ability };
        return { name: t.name || card.name, ability: t.ability || card.ability };
    };

    global.localizeCard = function (card, lang) {
        const f = global.getLocalizedCardFields(card, lang);
        return Object.assign({}, card, f);
    };

    global.applyCardGalleryLocalization = function (lang) {
        const nodes = document.querySelectorAll('.cards-gallery .gallery-item');
        if (!nodes.length || typeof CARDS_DATABASE === 'undefined') return;
        nodes.forEach((item, idx) => {
            const id = idx + 1;
            const base = CARDS_DATABASE.find(function (c) {
                return c.id === id;
            });
            if (!base) return;
            const L = global.getLocalizedCardFields(base, lang);
            const nameEl = item.querySelector('.card-name');
            const img = item.querySelector('.card-image');
            if (nameEl) nameEl.textContent = L.name;
            if (img) img.setAttribute('alt', L.name);
        });
    };

